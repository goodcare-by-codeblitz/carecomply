-- =============================================================================
-- PHASE 6: Restrict permissions table to org members only
-- =============================================================================

-- The previous policy allowed ALL authenticated users to enumerate every
-- permission code in the system. Replace it with a scoped read so users can
-- only see permissions relevant to at least one of their orgs' roles.
drop policy if exists "Authenticated users can view permissions" on public.permissions;
create policy "Org members can view relevant permissions"
  on public.permissions
  for select
  to authenticated
  using (
    exists (
      select 1
      from public.role_permissions rp
      join public.roles r on r.id = rp.role_id
      where rp.permission_id = permissions.id
        and r.organization_id = any(public.get_my_org_ids())
    )
  );

