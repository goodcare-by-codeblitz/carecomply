-- Returns all active organization IDs for the current user.
-- Used in RLS policies to replace repeated subqueries.
create or replace function public.get_my_org_ids()
returns uuid[]
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(
    array_agg(om.organization_id),
    '{}'::uuid[]
  )
  from public.organization_memberships om
  where om.user_id = auth.uid()
    and om.deleted_at is null
    and coalesce(om.status, 'active') in ('active', 'on_leave');
$$;

-- Returns true if the current user is a platform admin.
-- Equivalent to the middleware platform_memberships query.
create or replace function public.is_platform_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.platform_memberships
    where user_id = auth.uid()
  );
$$;

