-- =============================================================================
-- PHASE 9: Audit log immutability trigger
-- =============================================================================

-- Prevent any UPDATE or DELETE on audit_logs. Only INSERTs are allowed.
-- service_role bypasses RLS but NOT triggers, so this is a hard backstop.

create or replace function public.prevent_audit_log_modification()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  raise exception 'Audit log records are immutable and cannot be modified or deleted.';
end;
$$;

drop trigger if exists audit_logs_immutable on public.audit_logs;
create trigger audit_logs_immutable
  before update or delete on public.audit_logs
  for each row execute function public.prevent_audit_log_modification();

-- Also apply to auth_audit_events
drop trigger if exists auth_audit_events_immutable on public.auth_audit_events;
create trigger auth_audit_events_immutable
  before update or delete on public.auth_audit_events
  for each row execute function public.prevent_audit_log_modification();

