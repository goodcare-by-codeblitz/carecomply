-- =============================================================================
-- PHASE 4: PII encryption infrastructure
-- =============================================================================

-- Enable pgcrypto for AES-256 symmetric encryption.
-- Required for NI number, passport number, DBS certificate number fields.
create extension if not exists pgcrypto;

-- Encryption key: stored as a platform setting, NOT hardcoded here.
-- The functions below read from platform_settings at runtime. This avoids
-- baking secrets into the migration file itself.
-- Before deploying, insert the key:
--   insert into public.platform_settings (key, value)
--   values ('pii_encryption_key', '<strong-random-secret>')
--   on conflict (key) do nothing;

-- Encrypt a plaintext string. Returns NULL if input is NULL.
create or replace function public.pii_encrypt(p_plaintext text)
returns bytea
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_key text;
begin
  if p_plaintext is null then
    return null;
  end if;
  select value into v_key
  from public.platform_settings
  where key = 'pii_encryption_key';
  if v_key is null then
    raise exception 'pii_encryption_key not configured in platform_settings';
  end if;
  return extensions.pgp_sym_encrypt(p_plaintext, v_key);
end;
$$;

-- Decrypt a bytea ciphertext. Returns NULL if input is NULL.
create or replace function public.pii_decrypt(p_ciphertext bytea)
returns text
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_key text;
begin
  if p_ciphertext is null then
    return null;
  end if;
  select value into v_key
  from public.platform_settings
  where key = 'pii_encryption_key';
  if v_key is null then
    raise exception 'pii_encryption_key not configured in platform_settings';
  end if;
  return extensions.pgp_sym_decrypt(p_ciphertext, v_key);
end;
$$;

